# оптимизайия пути

print("Введите кол-во киллометров которое хотите пройти:")

x = int(input())

print("Введите расход топлива автомобиля на 100 км:")

y = int(input())

print("Введите кол-во топлива в баке:")

z = int(input())

k = x / 100 * y

if k > z:
    print("к сожалению вам не зватит топлива.")
elif k < z:
    print("Вам хватит ттоплива.")