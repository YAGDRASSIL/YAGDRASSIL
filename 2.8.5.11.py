# определяем весовую категорию боксера

print("Введите вес боксёра:")

x = int(input())

if x > 0 and x < 60:
    print("Боксёр относитьсся к лёгкой весвовй категории.")
elif x >= 60 and x < 64:
    print("Боксёр относиться к певрому полусреднему весу.")
elif x <= 64 and x < 69:
    print("Боксёр относиться к полусреднему весу.")
