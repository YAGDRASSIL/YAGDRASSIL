# расчитываем среднюю длинну кусков в окружности

import math

print("Введите диаметр кольца:")

i = int(input())

print("введите на сколько кусков хотите поделить кольцо:")

p = int(input)

k = i * math.pi * 2 # длинна кольца

l = k / (p + 1)

print("Средняя длинна получившегося кусков:", l)